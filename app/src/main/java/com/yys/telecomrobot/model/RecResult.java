public class RecResult {

    private String result;
    private Long ed;
    private Long bg;
    private String clg;
    private Long pgs;

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public Long getEd() {
        return ed;
    }

    public void setEd(Long ed) {
        this.ed = ed;
    }

    public Long getBg() {
        return bg;
    }

    public void setBg(Long bg) {
        this.bg = bg;
    }

    public Long getPgs() {
        return pgs;
    }

    public void setPgs(Long pgs) {
        this.pgs = pgs;
    }

    public String getClg() {
        return clg;
    }

    public void setClg(String clg) {
        this.clg = clg;
    }

}
